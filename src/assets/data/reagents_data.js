class ReagentProperty {
  code;
  name;
  colour;

  constructor(code, name, colour) {
    this.code = code;
    this.name = name;
    this.colour = colour;
  }
  get info() {
    return { code: this.code, name: this.name, colour: this.colour };
  }
}

